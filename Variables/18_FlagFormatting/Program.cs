Console.WriteLine("C format: {0:C}", 99.9);      // Pul formatı.
Console.WriteLine("N format: {0:N}", 99999);     // Standart ədadə formatlanması.
Console.WriteLine("X format: {0:X}", 255);       // 16-lıq say sistemində formatlanmıası.
Console.WriteLine("G format: {0:G}", 99.9);      // Ümumi format.
Console.WriteLine("P format: {0:P}", 99.9);      // Faiz şəklində format.

